#include "libft.h"

void				ft_bzero(void *s, size_t n)
{
	char unsigned	*tmp;
	size_t			a;

	tmp = s;
	a = 0;
	while (n > a && tmp[a])
	{
		tmp[a] = '\0';
		a++;
	}
}
